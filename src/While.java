
public class While {
	@SuppressWarnings("unused")
	public static void main(String[] args) {
		for(int i=0;i<6;i++) {
			System.out.println(i);
			
		}
		int number = 3;
		
		while(number>0) {
			System.out.println(number);
			number--;
		}
		
			int number1 = 3;
			do {
			
			System.out.println(number1);
			number1--;
		}while(number1>0);
}
}